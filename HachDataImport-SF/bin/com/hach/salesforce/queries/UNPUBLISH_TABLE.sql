  KEY `idx_TABLE_NAME_PLACEHOLDER_CHANNEL_REF_KEY` (`CHANNEL_REF_KEY`),
  KEY `idx_TABLE_NAME_PLACEHOLDER_SF_MASTER_IDENTIFIER` (`SF_MASTER_IDENTIFIER`),
  KEY `idx_TABLE_NAME_PLACEHOLDER_SF_PROCESSING_STATUS` (`SF_PROCESSING_STATUS`)
) ENGINE=InnoDB;
