  `LOCALE` varchar(20) CHARACTER SET utf8 COLLATE utf8_general_ci DEFAULT NULL,
  `CHANNEL` varchar(200) CHARACTER SET utf8 COLLATE utf8_general_ci DEFAULT NULL,
  `NAME` varchar(2000) CHARACTER SET utf8 COLLATE utf8_general_ci DEFAULT NULL,
